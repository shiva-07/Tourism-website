			<h4>Haridwar</h4>
            		<p>Haridwar is an ancient city and important Hindu pilgrimage site in North India's Uttarakhand state, where the 
			River Ganges exits the Himalayan foothills. The largest of several sacred ghats (bathing steps), Har Ki Pauri 
			hosts a nightly Ganga Aarti (river-worshipping ceremony) in which tiny flickering lamps 
			are floated off the steps.<br>------------<br><br>
<b>LOCAL STREET FOOD IN HARIDWAR</b><br>

<b>1.Aloo Puri:</b><br>
 Famous across North India, Aloo Puri is undoubtedly the best street food in Haridwar. It is not only the signature cuisine of the city, but also an all time favorite be it for lunch or dinner. The puris are deep fried Indian breads that are served with tangy yet spicy Aloo ki Sabzi, and often accompanied with halwa on the side. This is an absolute must-have when you’re holidaying in the city.
Best Places To Eat At: Mohanji Puri Wale, Chotiwala Restaurant, and Hoshiyarpuri<br>
 
<b>2.Kachori:</b><br>
Just like Delhi, kachori has been a famous street food in Haridwar as well. Served along with with sabzi and chutney, it can be enjoyed as a quick snack at any time of the day. Though, this is the most common breakfast of locals. So, if this is something you do not want to miss, get up early in the morning and head out to the restaurants at Har Ki Pauri Ghat for savouring fresh and toothsome kachoris.
Best Places To Eat At: Kashyap Kachori Bhandar at Gau Ghat, Bhagwati Chole Bhandar, and Chotiwala Restaurant<br>
 
<b>3.Rasmalai and Jalebi:</b><br>
Needless to say this city is a paradise for sweet lovers and sweets like Jalebi, Rasmalai, & Rabri are a part of the must-have Haridwar special food. The sweets here are mostly made with pure ghee and are only served fresh on the plate. Whether you eat one or many, we can vouch that you’d end up craving for more.
Best Place To Eat At: Mathura walon ki Pracheen Dukaan.<br>-----------<br><br>
 
 
<b>LOCAL SHOPPING CENTER IN HARIDWAR:</b><br>
<b>1.Tibetian Shopping center:</b><br>
The people willing to buy the authentic handicrafts of Tibet must shop from this place once in Haridwar holidays. Woolen dresses are the best to buy. You will get the original sheep wool and that to hand-knitted. The other woolen products you can buy are woolen blankets, scarves, shawls, and mufflers. There are many Tibetan souvenirs shops available here. Those shops are the best to buy traditional fashion accessories for kids, men, and women. You can bargain and come down on price. They do give discounts when you buy in bulk quantities.<br>
Address: Bhalla Road, Haridwar, Uttarakhand 249401<br>
Timing: 10:00 AM to 08:00 PM (closed on Wednesday)<br>
 
<b>2.Haridwar Achar Center:</b><br>
Achar or pickles of Haridwar are with some specialty. You have to buy and taste this to see the difference between the conventional pickles and homemade pickles. They are made traditionally. They do not contain any chemical preservatives. They sell achar made of vegetables and fruits only. You can taste a sample and buy one if you like its taste, flavor, and aroma. They are available in bottles and tight packing. They are priced according to weight. The prices of pickles are fixed. You can rarely bargain and come down on price. They are available as fresh pickles and old pickles. It is advisable to check the date of packing and expiry date before buying.<br>
Address: No. 16, Upper Rd, Har Ki Pauri, Haridwar, Uttarakhand 249401<br>
<b>3.Shiva Ratna Kendra:</b><br>
This is the authentic shop to buy the original pooja items once in the Haridwar pilgrim tour. You can buy a traditional bead necklace and bangles from this store. Either this store sells all Hindu religious accessory wear, which is the best to buy by pilgrim takers and to show sings of Hinduism in once body. They have gemstones, which you can buy as per your zodiac color. You can buy them as a ring, and necklace dollar. The pooja wear available here are for men and women. The pooja malas are with different designs and make. They are also available in combined forms like wood and metal malas.<br>
Address: Haridwar, Uttarakhand, Haridwar, Uttarakhand 249401
