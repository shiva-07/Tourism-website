			<h4>Ladakh</h4>
            <p>Ladakh ("land of high passes") is a region in northern India.
			It is located between the Kunlun mountain range in the north and the main Himalayas to the south.
			Ladakh is well-knownfor its remote mountain scenery..<br>----------------<br><br>
					<b>STREET FOOD IN LADAKH:</b><br>
<b>1. Momos:</b><br>
Momos without the doubt is the most popular dish in Ladhak. It is
a dumpling filled with meat or vegetable wrapped in a dough.
While moms stuffed with minced meat enjoys popularity among
locals, ve momos are also savoured alike.<br>

<b>2.Thukpa:</b><br>
Thukpa is the staple food of people of Ladhak. It is a soupy noodle
dish with assorted vegetables. The noodle is made of wheat or
barley flour. The broth, rich in spices, not only satitates the taste
buds but also warms the body. There are different types of
Thukpa served in different parts of Ladhak.<br>

<b>3.Skyu:</b><br>
Skyu is a soup-based traditional speciality. The dish contains
vegetable and dough kneaded into flattened thumb-sized balls.
These flattened balls are then put in a pot with water and
vegetables and cooked on low heat. Skyu is also served with meat.
The dish is high on calories and fills the stomach. Skyu is mostly
consumed in winter when temperature drops below freezing
point. It is also a favourite among tourists who do trekking and
stay in villages.<br>--------------<br><br>

<b>LOCAL SHOPPING CENTERS:</b><br>
<b>1.Moti market:</b><br>
Moti market near the Leh bus stand is a popular marlet. All
popular shopping item are available in this main market of leh,
the traditional Leh jewellery and high quality of shawls are the
popular items to shop for. Here you will get all products
accessories in budget price.<br>

<b>2.Zanskar Arts:</b><br>
Though Zanskar Arts is a quaint little shop located in the
primary market area of Leh, the fact that it is getting a special
mention is that it sells not only high-quality woolen garments
but souveneris and little figurines as well. The shop is not only
quite popular among the locals but the tourist as well due to
the softness of the shawls and the sweaters that are made of
pure Pashmina wool.</p>
