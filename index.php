			<h4>Andaman</h4>
            <p>Andaman and Nicobar Islands, union territory, India, consisting of two groups of islands at the southeastern 
			edge of the Bay of Bengal. The peaks of a submerged mountain range, the Andaman Islands and their neighbours to 
			the south, the Nicobar Islands, form an arc stretching southward for some 620 miles (1,000 km) between Myanmar
			(Burma) and the island of Sumatra, Indonesia. The arc constitutes the boundary between the Bay of Bengal to the 
			west and the Andaman Sea to the east. Port Blair (on South Andaman Island) is the territorial capita
			<br>-------------<br><br>

<b>LOCAL STREET FOOD IN ANDAMAN</b><br>

<b>1.Tandoori fish</b><br>
Andaman Nicobar Islands has unlimited resources when it comes to seafood so it is safe to say that it is the king of seafood varieties in India, tandoori fish being its specialty. It is widely available as all restaurants serve this dish, especially popular in the Emerald Island.<br>
<b>2.Coconut Prawn Curry</b><br>
What’s an island dish without some coconut in it, right? It doesn’t matter if you are a coconut lover or not, this dish is sure to leave you craving for more. The delightful taste of the prawns fused with the creamy texture of the Andaman style curry makes this dish a must-try for everyone. This, served with rice, after a long day at the beach, will feel nothing less than heaven on earth. Don’t miss out on this.
<b>3.BBQ food</b><br>
Since Andaman is in such close proximity to the ocean, Barbeque food is a part of the culture there and you definitely need to give it a try. Freshly caught fishes such as Sardines, King Prawns, Crab, etc are prepared and barbecued right in front of your eyes. You can relish it from anywhere in the local areas including the island, outdoor sightseeing or in the middle of the forests. So much so, you even have an option to have a BBQ in the center of the sea. Truly wild!<br>-------------<br><br>

<b>LOACL SHOPPING CENTERS IN ANDAMAN</b><br>
<b>1.Sagarika Government  Emporium</b><br>
 If you want to see some of the best creations of the locals then the Sagarika Government Emporium is the place to be at. This emporium offers a platform for the locals to showcase their talent and sell their creations. Wood, seashells and jute are the primary materials out of which these people make out stuff they sell here which is all hand made. From handicrafts to wooden items, mirrors and even apparels you will find everything here and the stuff they sell is authentic and so pretty that you won’t be able to resist.<br>
Location: DAG Colony, Port Blair, Andaman and Nicobar Islands<br>
<b>2.Andaman Handicraft Emporium:</b><br>
If some pretty handicraft items are what you have been looking for all this while, then the Andaman Handicraft Emporium is what you were looking out for all this while.One of the most famous markets in Andaman features this emporium and visiting this place should definitely be on your agenda if you plan on visiting Andaman. This place is a paradise for those looking for some pretty seashell articles and handicraft items.<br>
Location: Mg Road, Port Blair<br>
<b>3.Abardeen Bazar</b><br>
One of the best markets in Andaman, it’s not even possible that you haven’t heard about Aberdeen Bazaar. From the plain and simplest household goods to some very fancy and distinctive household goods you will find everything at this market. If you think you are a pro at bargaining then Aberdeen Bazaar is the place to check out the skills you hone. Your Andaman vacay will not be complete if you don’t visit this famous market in Andaman.<br>
Location: Aberdeen, Port Blair, Andaman and Nicobar Islands</p>
